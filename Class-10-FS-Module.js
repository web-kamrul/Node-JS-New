/*
fs বা File System মডিউল হলো Node.js-এর একটি অংশ, যা ফাইল সিস্টেমের সাথে সম্পর্কিত বিভিন্ন কাজ করতে সুযোগ দেয়। এটি ফাইল থেকে ডেটা পড়তে, ফাইলে ডেটা লেখতে, ডিরেক্টরি তৈরি করতে, এবং অনেক অন্যান্য কাজে ব্যবহার হয়।

এই মডিউলের কিছু প্রধান ফাংশন হলো:

fs.readFile(): এটি ফাইল থেকে ডেটা অ-সিনক্রোনাসভুক্তভাবে পড়ে।
fs.writeFile(): এটি ডেটা অ-সিনক্রোনাসভুক্তভাবে একটি ফাইলে লেখে।
fs.existsSync(): এটি একটি ফাইল বা ডিরেক্টরি কি আছে তা চেক করে।
fs.mkdir(): এটি একটি ডিরেক্টরি অ-সিনক্রোনাসভুক্তভাবে তৈরি করে।
fs.rmdir(): এটি একটি ডিরেক্টরি অ-সিনক্রোনাসভুক্তভাবে মুছে ফেলে।
এই উদাহরণগুলির মাধ্যমে fs মডিউল ব্যবহারের কিছু উদাহরণ দেখা গিয়েছে।
*/